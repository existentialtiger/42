int		ft_atoi(char *str)
{
	int isneg;
	int nb;
	int i;

	isneg = 1;
	if (str[0] == '-')
	{
		isneg = -1;
	}
	else if (str[0] >= '0' && str[0] <= '9')
	{
		nb = str[0] - '0';
	}
	else
	{
		return (0);
	}
	i = 1;
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + isneg * (str[i] - '0');
		i++;
	}
	return (nb);
}

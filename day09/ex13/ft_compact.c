int	ft_compact(char **tab, int length)
{
	int i;

	i = 0;
	while (i < length)
	{
		if (tab[i][0] == 0)
		{
			tab[i][0] == tab[i + 1][0];
			length--;
		}
		i++;
	}
	return (length);
}
